x = 10
# Left shift operation denoted by << symbol
# Left shift operation shifts the bits in left
# direction and the resulting value will be equivalent
# to Multiplication by 2
print(x<<2)
print(x<<1<<1)

# Right shift operation denoted by >> symbol
# Right shift operation shifts the bits in right
# direction and the resulting value will be equivalent
# to Division by 2
print(x>>1>>1>>1>>1)
print(x>>3)